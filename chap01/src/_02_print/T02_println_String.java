package _02_print;

import java.util.*;

public class T02_println_String {

	public static void main(String[] args){
	       
        Scanner sc = new Scanner(System.in);
        
        int m;
        
        int K = sc.nextInt();
        
        for(m = 0; 0 < K; K--){
            m = m+K;
        }
        
        System.out.print(m);
    }
}
